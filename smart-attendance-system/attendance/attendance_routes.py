def _normalize_subject(raw_subject):
    subject = (raw_subject or "General").strip()
    return subject[:60] if subject else "General"


def _parse_qr_payload(raw_qr, fallback_subject):
    qr_data = (raw_qr or "").strip()
    if not qr_data:
        return None, None

    subject = _normalize_subject(fallback_subject)

    try:
        data = json.loads(qr_data)
        code = (data.get("student_code") or data.get("unique_id") or "").strip()
        subject = _normalize_subject(data.get("subject") or subject)
        return code, subject
    except Exception:
        pass

    if "|" in qr_data:
        left, right = qr_data.split("|", 1)
        return left.strip(), _normalize_subject(right)

    if ":" in qr_data:
        left, right = qr_data.split(":", 1)
        return left.strip(), _normalize_subject(right)

    return qr_data, subject


def _qr_serializer():
    return URLSafeTimedSerializer(current_app.config["SECRET_KEY"])


def _build_session_qr_token(subject):
    subject = _normalize_subject(subject)
    data = {"kind": "session", "subject": subject}
    return _qr_serializer().dumps(data, salt="attendance-qr")


def _make_scanner_qr_value(token):
    configured_base = (current_app.config.get("PUBLIC_BASE_URL") or "").strip().rstrip("/")
    if configured_base:
        base = configured_base
    else:
        parsed = urlparse(request.host_url)
        host = (parsed.hostname or "").lower()
        port = parsed.port
        scheme = parsed.scheme or "http"

        if host in ("127.0.0.1", "localhost"):
            try:
                sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
                sock.connect(("8.8.8.8", 80))
                lan_ip = sock.getsockname()[0]
                sock.close()
                if lan_ip and lan_ip not in ("127.0.0.1", "0.0.0.0"):
                    base = f"{scheme}://{lan_ip}:{port}" if port else f"{scheme}://{lan_ip}"
                else:
                    base = request.host_url.rstrip("/")
            except Exception:
                try:
                    lan_ip = socket.gethostbyname(socket.gethostname())
                    if lan_ip and lan_ip not in ("127.0.0.1", "0.0.0.0"):
                        base = f"{scheme}://{lan_ip}:{port}" if port else f"{scheme}://{lan_ip}"
                    else:
                        base = request.host_url.rstrip("/")
                except Exception:
                    base = request.host_url.rstrip("/")
        else:
            base = request.host_url.rstrip("/")

    path = url_for("attendance.qr_scan")
    return f"{base}{path}?t={quote_plus(token)}"


def _extract_qr_token_candidate(raw_qr):
    qr_data = (raw_qr or "").strip()
    if not qr_data:
        return ""

    if qr_data.startswith("SAQR|"):
        return qr_data.split("|", 1)[1].strip()

    parsed = urlparse(qr_data)
    if parsed.scheme in ("http", "https") and parsed.query:
        query = parse_qs(parsed.query)
        for key in ("t", "token", "qr", "qr_token"):
            values = query.get(key)
            if values and values[0].strip():
                return values[0].strip()

    return qr_data


def _read_qr_token_with_reason(token, max_age_seconds=QR_TOKEN_TTL_SECONDS):
    if not token:
        return None, "QR token missing"
    try:
        data = _qr_serializer().loads(token, salt="attendance-qr", max_age=max_age_seconds)
        if isinstance(data, dict):
            # New session token generated by teacher/admin.
            if data.get("kind") == "session" and data.get("subject"):
                return _normalize_subject(data.get("subject", "General")), None
            # Backward compatibility for old student-bound tokens.
            if data.get("student_id") and data.get("subject"):
                return _normalize_subject(data.get("subject", "General")), None
        return None, "Invalid QR payload"
    except SignatureExpired:
        return None, "QR expired. Generate a new QR and scan again."
    except (BadSignature, ValueError, TypeError):
        return None, "Invalid QR token"


def _read_student_qr_token(token, max_age_seconds=QR_TOKEN_TTL_SECONDS):
    try:
        data = _qr_serializer().loads(token, salt="attendance-qr", max_age=max_age_seconds)
        if isinstance(data, dict):
            if data.get("student_id"):
                return int(data["student_id"]), _normalize_subject(data.get("subject", "General"))
            if data.get("kind") == "session" and data.get("subject"):
                return None, _normalize_subject(data.get("subject", "General"))
    except (BadSignature, SignatureExpired, ValueError, TypeError):
        return None, None
    return None, None


def _current_student_id():
    student_id = session.get("student_id")
    if student_id:
        return int(student_id)
    if session.get("role") != "student":
        return None

    db = get_db()
    row = db.execute("SELECT student_id FROM users WHERE id = ?", (session.get("user_id"),)).fetchone()
    if not row or not row["student_id"]:
        return None
    session["student_id"] = row["student_id"]
    return int(row["student_id"])


