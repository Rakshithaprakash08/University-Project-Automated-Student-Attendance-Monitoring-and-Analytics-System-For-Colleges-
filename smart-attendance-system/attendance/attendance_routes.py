def mark_attendance(student_id, status="present", mode="manual", note="", subject="General", manual_override=False):
    db = get_db()
    now_dt = datetime.now()
    today = date_iso()
    ts = now_iso()
    weekday = now_dt.weekday()
    current_hhmm = now_dt.strftime("%H:%M")
    recent_cutoff_ts = (now_dt - timedelta(minutes=20)).strftime("%Y-%m-%d %H:%M:%S")
    subject = _normalize_subject(subject)
    subject = _resolve_active_subject_for_mark(student_id, subject, mode)
    period_for_match = _get_period_for_student_subject(student_id, subject, weekday=weekday, current_hhmm=current_hhmm)
    if period_for_match:
        existing = db.execute(
            """
            SELECT id, status, mode, note, unique_id_done, face_done, qr_done
            FROM attendance
            WHERE student_id = ? AND date = ? AND subject = ?
              AND substr(timestamp, 12, 5) >= ?
              AND substr(timestamp, 12, 5) <= ?
            ORDER BY id DESC
            LIMIT 1
            """,
            (student_id, today, subject, period_for_match["start_time"], period_for_match["end_time"]),
        ).fetchone()
        # If no row is found in the active period window, fall back to today's
        # subject row so previously completed methods are not lost.
        if not existing:
            existing = db.execute(
                """
                SELECT id, status, mode, note, unique_id_done, face_done, qr_done
                FROM attendance
                WHERE student_id = ? AND date = ? AND subject = ?
                ORDER BY id DESC
                LIMIT 1
                """,
                (student_id, today, subject),
            ).fetchone()
    else:
        existing = db.execute(
            """
            SELECT id, status, mode, note, unique_id_done, face_done, qr_done
            FROM attendance
            WHERE student_id = ? AND date = ? AND subject = ?
            ORDER BY id DESC
            LIMIT 1
            """,
            (student_id, today, subject),
        ).fetchone()

    flags = {
        "unique_id": int(existing["unique_id_done"]) if existing else 0,
        "face": int(existing["face_done"]) if existing else 0,
        "qr": int(existing["qr_done"]) if existing else 0,
    }

    # Manual/auto-absent placeholder rows should not carry prior
    # biometric/QR completions into a new live marking attempt.
    if existing:
        is_manual_baseline = existing["mode"] == "manual" and existing["status"] in ("absent", "late")
        is_auto_absent_note = str(existing["note"] or "").startswith("Auto absent")
        if is_manual_baseline or is_auto_absent_note:
            flags = {"unique_id": 0, "face": 0, "qr": 0}

    previous_flags = dict(flags)
    if mode in REQUIRED_METHODS:
        flags[mode] = 1

    prev_all_done = all(previous_flags[m] == 1 for m in REQUIRED_METHODS)
    all_done = all(flags[m] == 1 for m in REQUIRED_METHODS)
    newly_completed = all_done and not prev_all_done
    pending_methods = [m for m in REQUIRED_METHODS if flags[m] != 1]
    period = _get_period_for_student_subject(student_id, subject, weekday=weekday, current_hhmm=current_hhmm)
    period_closed = bool(period and current_hhmm > period["end_time"])

    if mode == "manual":
        if manual_override:
            if status in ("absent", "late"):
                flags = {"unique_id": 0, "face": 0, "qr": 0}
                all_done = False
                pending_methods = list(REQUIRED_METHODS)
            final_status = status
        else:
            final_status = "present" if all_done else "absent"
    else:
        final_status = "present" if all_done else "absent"

    if period_closed and not manual_override and not all_done:
        note = (note + " | " if note else "") + f"Period closed at {period['end_time']}; pending required methods"

    if existing:
        db.execute(
            """
            UPDATE attendance
            SET status = ?, mode = ?, unique_id_done = ?, face_done = ?, qr_done = ?, timestamp = ?, synced = 0, note = ?, subject = ?
            WHERE id = ?
            """,
            (final_status, mode, flags["unique_id"], flags["face"], flags["qr"], ts, note, subject, existing["id"]),
        )
        attendance_id = existing["id"]
        operation = "update"
    else:
        try:
            cursor = db.execute(
                """
                INSERT INTO attendance (student_id, date, subject, status, mode, unique_id_done, face_done, qr_done, timestamp, synced, note)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, 0, ?)
                """,
                (student_id, today, subject, final_status, mode, flags["unique_id"], flags["face"], flags["qr"], ts, note),
            )
            attendance_id = cursor.lastrowid
            operation = "insert"
        except sqlite3.IntegrityError:
            # Another request created the row first (same student/date/subject unique key); update it instead.
            winner = db.execute(
                """
                SELECT id, status, mode, note, unique_id_done, face_done, qr_done, timestamp
                FROM attendance
                WHERE student_id = ? AND date = ? AND subject = ?
                ORDER BY id DESC
                LIMIT 1
                """,
                (student_id, today, subject),
            ).fetchone()
            if not winner:
                raise

            # Merge winner flags only for near-time collisions, not stale rows.
            can_merge_winner = bool(
                winner["timestamp"]
                and not (winner["mode"] == "manual" and winner["status"] in ("absent", "late"))
                and not str(winner["note"] or "").startswith("Auto absent")
            )
            if can_merge_winner:
                flags["unique_id"] = max(int(winner["unique_id_done"] or 0), flags["unique_id"])
                flags["face"] = max(int(winner["face_done"] or 0), flags["face"])
                flags["qr"] = max(int(winner["qr_done"] or 0), flags["qr"])
                all_done = all(flags[m] == 1 for m in REQUIRED_METHODS)
                pending_methods = [m for m in REQUIRED_METHODS if flags[m] != 1]
                final_status = "present" if all_done else "absent"

            db.execute(
                """
                UPDATE attendance
                SET status = ?, mode = ?, unique_id_done = ?, face_done = ?, qr_done = ?, timestamp = ?, synced = 0, note = ?, subject = ?
                WHERE id = ?
                """,
                (final_status, mode, flags["unique_id"], flags["face"], flags["qr"], ts, note, subject, winner["id"]),
            )
            attendance_id = winner["id"]
            operation = "update"

    payload = {
        "attendance_id": attendance_id,
        "student_id": student_id,
        "date": today,
        "subject": subject,
        "status": final_status,
        "mode": mode,
        "unique_id_done": flags["unique_id"],
        "face_done": flags["face"],
        "qr_done": flags["qr"],
        "previous_unique_id_done": previous_flags["unique_id"],
        "previous_face_done": previous_flags["face"],
        "previous_qr_done": previous_flags["qr"],
        "all_methods_completed": all_done,
        "newly_completed": newly_completed,
        "pending_methods": pending_methods,
        "timestamp": ts,
        "note": note,
    }
    db.execute(
        "INSERT INTO sync_queue (entity_type, entity_id, operation, payload) VALUES (?, ?, ?, ?)",
        ("attendance", attendance_id, operation, to_json(payload)),
    )
    db.commit()

    return {
        "attendance_id": attendance_id,
        "mode": mode,
        "subject": subject,
        "status": final_status,
        "unique_id_done": flags["unique_id"],
        "face_done": flags["face"],
        "qr_done": flags["qr"],
        "previous_unique_id_done": previous_flags["unique_id"],
        "previous_face_done": previous_flags["face"],
        "previous_qr_done": previous_flags["qr"],
        "all_methods_completed": all_done,
        "newly_completed": newly_completed,
        "pending_methods": pending_methods,
        "time": datetime.now().strftime("%H:%M:%S"),
    }

