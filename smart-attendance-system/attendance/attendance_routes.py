def _get_period_for_student_subject(student_id, subject, weekday=None, current_hhmm=None):
    db = get_db()
    if weekday is None:
        weekday = datetime.now().weekday()
    if current_hhmm is None:
        current_hhmm = datetime.now().strftime("%H:%M")

    active = db.execute(
        """
        SELECT t.id, t.subject, t.start_time, t.end_time, s.section_name
        FROM students s
        JOIN timetable t ON t.section_name = s.section_name
        WHERE s.id = ? AND s.active = 1 AND t.weekday = ? AND t.subject = ?
          AND t.start_time <= ? AND t.end_time >= ?
        ORDER BY t.start_time
        LIMIT 1
        """,
        (student_id, int(weekday), subject, current_hhmm, current_hhmm),
    ).fetchone()
    if active:
        return active

    # Fallback: nearest matching period for the day (upcoming first, then latest ended).
    upcoming = db.execute(
        """
        SELECT t.id, t.subject, t.start_time, t.end_time, s.section_name
        FROM students s
        JOIN timetable t ON t.section_name = s.section_name
        WHERE s.id = ? AND s.active = 1 AND t.weekday = ? AND t.subject = ?
          AND t.end_time >= ?
        ORDER BY t.start_time
        LIMIT 1
        """,
        (student_id, int(weekday), subject, current_hhmm),
    ).fetchone()
    if upcoming:
        return upcoming

    return db.execute(
        """
        SELECT t.id, t.subject, t.start_time, t.end_time, s.section_name
        FROM students s
        JOIN timetable t ON t.section_name = s.section_name
        WHERE s.id = ? AND s.active = 1 AND t.weekday = ? AND t.subject = ?
        ORDER BY t.end_time DESC
        LIMIT 1
        """,
        (student_id, int(weekday), subject),
    ).fetchone()


def _resolve_active_subject_for_mark(student_id, requested_subject, mode):
    """Prefer a recent in-progress row for the same student/method.

    This keeps method-order behavior consistent: whichever method is marked first,
    the next method marks the same active row and only true pending methods remain.
    """
    if mode not in REQUIRED_METHODS:
        return _normalize_subject(requested_subject)

    db = get_db()
    now_dt = datetime.now()
    today = date_iso()
    recent_cutoff_ts = (now_dt - timedelta(minutes=20)).strftime("%Y-%m-%d %H:%M:%S")
    requested_subject = _normalize_subject(requested_subject)

    recent_rows = db.execute(
        """
        SELECT subject, status, mode, note, unique_id_done, face_done, qr_done
        FROM attendance
        WHERE student_id = ?
          AND date = ?
          AND timestamp >= ?
        ORDER BY id DESC
        LIMIT 10
        """,
        (student_id, today, recent_cutoff_ts),
    ).fetchall()

    for row in recent_rows:
        is_manual_baseline = row["mode"] == "manual" and row["status"] in ("absent", "late")
        is_auto_absent_note = str(row["note"] or "").startswith("Auto absent")
        if is_manual_baseline or is_auto_absent_note:
            continue

        flags = {
            "unique_id": int(row["unique_id_done"] or 0),
            "face": int(row["face_done"] or 0),
            "qr": int(row["qr_done"] or 0),
        }

        any_done = any(flags[m] == 1 for m in REQUIRED_METHODS)
        all_done = all(flags[m] == 1 for m in REQUIRED_METHODS)
        current_pending = flags[mode] != 1

        if any_done and not all_done and current_pending and row["subject"]:
            return _normalize_subject(row["subject"])

    return requested_subject


def _apply_auto_absences(now_dt=None):
    db = get_db()
    now_dt = now_dt or datetime.now()
    today = date_iso()
    weekday = now_dt.weekday()
    current_hhmm = now_dt.strftime("%H:%M")
    ts = now_iso()

    due_rows = db.execute(
        """
        SELECT s.id AS student_id, t.subject, t.end_time
        FROM timetable t
        JOIN students s ON s.section_name = t.section_name
        WHERE s.active = 1
          AND t.weekday = ?
          AND t.end_time <= ?
        """,
        (weekday, current_hhmm),
    ).fetchall()

    created_count = 0
    updated_count = 0
    for row in due_rows:
        existing = db.execute(
            "SELECT id, status FROM attendance WHERE student_id = ? AND date = ? AND subject = ?",
            (row["student_id"], today, row["subject"]),
        ).fetchone()

        if existing:
            if existing["status"] != "present":
                note = f"Auto absent: not present before period end {row['end_time']}"
                db.execute(
                    """
                    UPDATE attendance
                    SET status = 'absent', mode = 'manual', unique_id_done = 0, face_done = 0, qr_done = 0, timestamp = ?, synced = 0, note = ?
                    WHERE id = ?
                    """,
                    (ts, note, existing["id"]),
                )
                db.execute(
                    "INSERT INTO sync_queue (entity_type, entity_id, operation, payload) VALUES (?, ?, ?, ?)",
                    (
                        "attendance",
                        existing["id"],
                        "update",
                        to_json(
                            {
                                "attendance_id": existing["id"],
                                "student_id": row["student_id"],
                                "date": today,
                                "subject": row["subject"],
                                "status": "absent",
                                "mode": "manual",
                                "unique_id_done": 0,
                                "face_done": 0,
                                "qr_done": 0,
                                "all_methods_completed": False,
                                "pending_methods": list(REQUIRED_METHODS),
                                "timestamp": ts,
                                "note": note,
                            }
                        ),
                    ),
                )
                updated_count += 1
            continue

        note = f"Auto absent: no present mark before period end {row['end_time']}"
        cursor = db.execute(
            """
            INSERT INTO attendance (student_id, date, subject, status, mode, unique_id_done, face_done, qr_done, timestamp, synced, note)
            VALUES (?, ?, ?, 'absent', 'manual', 0, 0, 0, ?, 0, ?)
            """,
            (row["student_id"], today, row["subject"], ts, note),
        )
        attendance_id = cursor.lastrowid
        db.execute(
            "INSERT INTO sync_queue (entity_type, entity_id, operation, payload) VALUES (?, ?, ?, ?)",
            (
                "attendance",
                attendance_id,
                "insert",
                to_json(
                    {
                        "attendance_id": attendance_id,
                        "student_id": row["student_id"],
                        "date": today,
                        "subject": row["subject"],
                        "status": "absent",
                        "mode": "manual",
                        "unique_id_done": 0,
                        "face_done": 0,
                        "qr_done": 0,
                        "all_methods_completed": False,
                        "pending_methods": list(REQUIRED_METHODS),
                        "timestamp": ts,
                        "note": note,
                    }
                ),
            ),
        )
        created_count += 1

    if created_count or updated_count:
        db.commit()
    return created_count + updated_count


@attendance_bp.route("/timetable", methods=["GET", "POST"])
@login_required
@role_required("admin")
def timetable_page():
    db = get_db()
    if request.method == "POST":
        section_name = request.form.get("section_name", "").strip()
        weekday = request.form.get("weekday", type=int)
        subject = _normalize_subject(request.form.get("subject", "General"))
        start_time = (request.form.get("start_time", "") or "").strip()
        end_time = (request.form.get("end_time", "") or "").strip()

        if not section_name or weekday is None or not start_time or not end_time:
            flash("Section, weekday, subject, start and end time are required", "error")
            return redirect(url_for("attendance.timetable_page"))
        if len(start_time) != 5 or len(end_time) != 5 or start_time >= end_time:
            flash("Enter valid HH:MM times and ensure end time is after start time", "error")
            return redirect(url_for("attendance.timetable_page"))

        try:
            db.execute(
                """
                INSERT INTO timetable (section_name, weekday, subject, start_time, end_time)
                VALUES (?, ?, ?, ?, ?)
                """,
                (section_name, weekday, subject, start_time, end_time),
            )
            db.commit()
            flash("Timetable period added", "success")
        except Exception:
            flash("This section already has the same subject for that weekday", "error")
        return redirect(url_for("attendance.timetable_page"))

    sections = db.execute("SELECT DISTINCT section_name FROM students WHERE active = 1 ORDER BY section_name").fetchall()
    entries = db.execute(
        "SELECT id, section_name, weekday, subject, start_time, end_time FROM timetable ORDER BY section_name, weekday, start_time"
    ).fetchall()
    weekday_map = {idx: name for idx, name in WEEKDAY_OPTIONS}
    return render_template(
        "timetable.html",
        sections=[r["section_name"] for r in sections],
        entries=entries,
        weekday_options=WEEKDAY_OPTIONS,
        weekday_map=weekday_map,
    )


